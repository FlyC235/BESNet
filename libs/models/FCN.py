def Upsample(x, size):
    """
    Wrapper Around the Upsample Call
    """
    return nn.functional.interpolate(x, size=size, mode='bilinear',
                                     align_corners=True)

class FCNHead(nn.Module):
    def __init__(self, in_channels, out_channels, norm_layer=nn.BatchNorm2d):
        super(FCNHead, self).__init__()
        inter_channels = in_channels // 4
        self.conv5 = nn.Sequential(nn.Conv2d(in_channels, inter_channels, 3, padding=1, bias=False),
                                    norm_layer(inter_channels),
                                    nn.ReLU(),
                                    nn.Dropout(0.1, False),
                                    nn.Conv2d(inter_channels, out_channels, 1))

        for m in self.modules():
            if isinstance(m, nn.Conv2d):
                nn.init.kaiming_normal_(m.weight, mode='fan_out')
                if m.bias is not None:
                    nn.init.constant_(m.bias, 0)
            elif isinstance(m, nn.BatchNorm2d):
                nn.init.constant_(m.weight, 1)
                nn.init.constant_(m.bias, 0)

    def forward(self, x):
        return self.conv5(x)
  
class BESNet(nn.Module):
    def __init__(self, nclass, backbone='resnet18', aux=True, norm_layer=nn.BatchNorm2d, pretrained=True):
        super(BESNet, self).__init__()

        self.aux=aux
        resnet = eval(backbone)(pretrained=pretrained)

        self.layer0 = nn.Sequential(
            resnet.conv1,
            resnet.bn1,
            resnet.relu,
            resnet.maxpool)
        
        self.layer1 = resnet.layer1
        self.layer2 = resnet.layer2
        self.layer3 = resnet.layer3
        self.layer4 = resnet.layer4
       
        for n, m in self.layer3.named_modules():
            if 'conv2' in n:
                m.dilation, m.padding, m.stride = (2, 2), (2, 2), (1, 1)
            elif 'downsample.0' in n:
                m.stride = (1, 1)
        for n, m in self.layer4.named_modules():
            if 'conv2' in n:
                m.dilation, m.padding, m.stride = (4, 4), (4, 4), (1, 1)
            elif 'downsample.0' in n:
                m.stride = (1, 1)

        self.BoundaryExtraction = BE_Module(64, 64, 512, 64, 128, 1)

        self.Fusion = MSF_Module([128, 256, 512], 128, 256, 128, 128)

        self.up = UP_Conv(128, 128)

        self.Enhance = BES_Module(512, 128)

        self.head = FCNHead(128, nclass, norm_layer)
        if self.aux:
            self.auxlayer = FCNHead(256, nclass, norm_layer)
        
    def forward(self, x):
        
        imsize = x.size()[2:]

        c0 = x = self.layer0(x)
        c1 = x = self.layer1(x)
        c2 = x = self.layer2(x)
        c3 = x = self.layer3(x)
        c4 = x = self.layer4(x)

        b, c_boundaryloss = self.BoundaryExtraction(c0, c1, c4)

        f =  self.Fusion(c2, c3, c4) 
        f = self.up(f)

        x = self.Enhance(c4, b, f)
        
        x = self.head(x)
        x = Upsample(x, imsize)

        outputs = [x]
        if self.aux:
            auxout = self.auxlayer(c3)
            auxout = Upsample(auxout, imsize)
            outputs.append(auxout)
        
        if self.training and self.aux:
            outputs.append(c_boundaryloss)
            return tuple(outputs)
        return x, c_boundaryloss

